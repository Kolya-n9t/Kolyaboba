public class PostBox {

    int height;
    int length;
    int width;
    int weight;

    public PostBox(int height, int length, int width, int weight) {
        this.height = height;
        this.length = length;
        this.width = width;
        this.weight = weight;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(String name) {
        this.height = height;
    }

    public int getLength() {
        return length;
    }

    public void setLength(String name) {
        this.length = length;
    }

    public int getWidth() {
        return width;
    }

    public void setWidth(String name) {
        this.width = width;
    }

    public int getWeight() {
        return weight;
    }

    public void setWeight(String name) {
        this.weight = weight;
    }
}
